package com.example.kaixuan.recordcoursedetails;

public class ItemTypeA {
    private String courseName;
    private String teachingTime;
    private String teachingDuration;

    public String getCourseName() {
        return courseName;
    }

    public String getTeachingTime() {
        return teachingTime;
    }

    public String getTeachingDuration() {
        return teachingDuration;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public void setTeachingTime(String teachingTime) {
        this.teachingTime = teachingTime;
    }

    public void setTeachingDuration(String teachingDuration) {
        this.teachingDuration = teachingDuration;
    }
}
